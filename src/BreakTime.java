public class BreakTime {
    public static void main(String[] args) {
        // Declare a boolean for break time
        boolean isBreakTime = true;

        // Check if it's break time and print a message
        if (isBreakTime) {
            System.out.println("Break Time! ☕🍪");
            System.out.println("Time to recharge and get back to coding!");
        } else {
            System.out.println("Not break time yet. Keep coding!");
        }
    }
}
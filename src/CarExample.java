public class CarExample {

    public static void main(String[] args) {

        // Declare and initialize the car's make (brand)
        String make = "BMW";  // The brand of the car

        // Declare and initialize the manufacturing year of the car
        int year = 2025;  // The year the car was manufactured

        // Declare and initialize the car's color
        String color = "Black";  // The color of the car

        // Declare and initialize the car's engine type
        String engineType = "Hybrid";  // The type of engine the car uses

        // Display the details of the car
        System.out.println("Car Details: " +
                "Make: " + make + ", " +
                "Year: " + year + ", " +
                "Color: " + color + ", " +
                "Engine Type: " + engineType);
    }
}

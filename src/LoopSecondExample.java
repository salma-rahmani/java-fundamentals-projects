/*
Write a loop to calculate the sum of numbers from 1 to 50.
 */
public class LoopSecondExample {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 50; i++) {
            sum += i;
        }
        System.out.print("The sum of numbers from 1 to 50 is: " + sum);


    }
}

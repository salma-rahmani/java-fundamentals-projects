/*
Print multiplication table: Use a for loop to print the multiplication table for any number (e.g., 5).
 */
public class LoopForthExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            int multiplication = i * 5;
            System.out.println(i + " * " + "5" + "= " + multiplication);
        }

    }
}

public class AreaCalculation {

    /*
    6 Area Calculation:
    You're building a simple application to calculate the area of a rectangle.
    Define two variables length = 7 and width = 5.
    Calculate the area and store it in a variable called area.
    Display the result as: "The area of the rectangle with length [length] and width [width] is [area]."
    */

    public static void main(String[] args) {

        // Define the length of the rectangle
        int length = 7;  // Length of the rectangle

        // Define the width of the rectangle
        int width = 5;  // Width of the rectangle

        // Calculate the area of the rectangle
        int area = length * width;  // Area is calculated as length multiplied by width

        // Display the area of the rectangle
        System.out.println("The area of the rectangle with length " + length + " and width " + width + " is " + area);
    }
}

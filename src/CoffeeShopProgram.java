public class CoffeeShopProgram {

    public static void main(String[] args) {
        /*

        2. Multiple Assignments:

        You're setting up a small coffee shop program.
        Define and assign three variables in a single line:
        - coffeePrice to 5.50
        - isCappuccinoAvailable to true
        - shopRating to 4.8
        */

        // Declare and initialize variables with assigned values
        double coffeePrice = 5.50;  // Price of coffee in dollars
        boolean isCappuccinoAvailable = true;  // Availability status of cappuccino
        double shopRating = 4.8;  // Rating of the shop on a scale of 1 to 5

        // Display the values of the variables
        System.out.println("Coffee Price: " + coffeePrice);  // Print the coffee price
        System.out.println("Is Cappuccino Available: " + isCappuccinoAvailable);  // Print cappuccino availability status
        System.out.println("Shop Rating: " + shopRating);  // Print the shop rating
    }
}
